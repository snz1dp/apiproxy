"""自定义异常定义."""

from typing import Optional


class NodeModelQuotaError(Exception):
    """节点模型配额相关基础异常。"""


class NodeModelQuotaExceeded(NodeModelQuotaError):
    """节点模型配额耗尽异常。"""

    def __init__(self, message: str, *, detail: Optional[str] = None) -> None:
        super().__init__(message)
        self.detail = detail
